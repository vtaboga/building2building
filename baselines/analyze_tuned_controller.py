def rollout_and_analyze(
    building_type: str,
    building_id: str,
    climate_zone: int,
    task: str,
    run_period: Literal["full_year", "winter", "summer"] = "full_year",
) -> RolloutArtifacts:
    env = b2b.make_env(
        building_type,
        building_id=building_id,
        task=task,
        run_period=run_period,
    )
    try:
        obs_names: list[str] = env.metadata["observation_names"]
        act_names: list[str] = env.metadata["action_names"]
        # ``morphology.controlled_zones`` is populated by build_morphology.
        morphology = env.metadata.get("morphology")
        controlled_zones: list[str] = []
        if morphology is not None and hasattr(morphology, "controlled_zones"):
            controlled_zones = list(morphology.controlled_zones)  # type: ignore[arg-type]
        elif morphology is not None and hasattr(morphology, "nodes"):
            for node in morphology.nodes:
                nid = getattr(node, "id", "")
                if nid.startswith("zone:"):
                    controlled_zones.append(nid[len("zone:") :])

        logger.info(
            "Rollout: %s / %s (cz=%d, task=%s) — zones=%d, run_period=%s",
            building_type,
            building_id,
            climate_zone,
            task,
            len(controlled_zones),
            run_period,
        )
        policy = _select_policy(building_type, climate_zone, task, env)

        def _controller(obs: np.ndarray) -> np.ndarray:
            action, _ = policy.predict(obs, deterministic=True)
            return action

        traj = b2b.rollout(env, _controller)
        observations = traj.observations
        rewards = traj.rewards
        actions = traj.actions
    finally:
        env.close()

    zone_indices, zone_labels = _zone_temp_indices(obs_names)
    sp_indices, sp_labels = _setpoint_indices(obs_names)
    out_t_idx = _find_index(obs_names, "outdoor_temperature")

    zone_temps = (
        observations[:, zone_indices].astype(np.float32)
        if zone_indices
        else np.zeros((observations.shape[0], 0), np.float32)
    )
    # Align setpoint columns to zone order by name.
    sp_by_zone = {label: i for i, label in zip(sp_indices, sp_labels)}
    aligned_sp_cols = []
    for zl in zone_labels:
        if zl in sp_by_zone:
            aligned_sp_cols.append(observations[:, sp_by_zone[zl]])
        else:
            aligned_sp_cols.append(np.full(observations.shape[0], np.nan, np.float32))
    setpoints = np.stack(aligned_sp_cols, axis=1).astype(np.float32)

    outdoor = (
        observations[:, out_t_idx].astype(np.float32)
        if out_t_idx is not None
        else np.zeros(observations.shape[0], np.float32)
    )
    elec, gas = _extract_energy_series(observations, obs_names)

    controlled_cols = [
        j for j, zl in enumerate(zone_labels) if zl in set(controlled_zones)
    ]
    if controlled_cols:
        zc = zone_temps[:, controlled_cols]
        sc = setpoints[:, controlled_cols]
    else:
        zc = zone_temps
        sc = setpoints

    # Fill NaN setpoints (zones without an explicit target) with their zone
    # temperature so the MSE contribution is zero.
    mask = np.isnan(sc)
    if mask.any():
        sc = np.where(mask, zc, sc)

    temp_term_full, energy_term_full = _decompose_reward(zc, sc, elec, gas)
    # Reward applies to post-step state → slice to align with actions.
    temp_term = temp_term_full[1:]
    energy_term = energy_term_full[1:]

    return RolloutArtifacts(
        zone_temperatures=zone_temps,
        zone_names=zone_labels,
        outdoor_temperature=outdoor,
        energy_electricity=elec,
        energy_gas=gas,
        actions=actions.astype(np.float32),
        action_names=act_names,
        rewards=rewards.astype(np.float32),
        temp_term=temp_term,
        energy_term=energy_term,
        setpoints=setpoints,
        controlled_zones=controlled_zones,
        building_type=building_type,
        building_id=building_id,
        climate_zone=climate_zone,
        task=task,
    )


def save_artifacts(
    art: RolloutArtifacts, output_dir: Path, deadband_c: float = 1.0
) -> dict[str, Any]:
    output_dir.mkdir(parents=True, exist_ok=True)
    stem = f"{art.building_id}_cz{art.climate_zone}"
    npz_path = output_dir / f"{stem}.npz"
    png_path = output_dir / f"{stem}_analysis.png"
    json_path = output_dir / f"{stem}.report.json"

    meta_dict = {
        "building_type": art.building_type,
        "building_id": art.building_id,
        "climate_zone": art.climate_zone,
        "task": art.task,
        "zone_names": art.zone_names,
        "controlled_zones": art.controlled_zones,
        "action_names": art.action_names,
    }
    np.savez_compressed(
        npz_path,
        zone_temperatures=art.zone_temperatures,
        outdoor_temperature=art.outdoor_temperature,
        energy_electricity=art.energy_electricity,
        energy_gas=art.energy_gas,
        actions=art.actions,
        rewards=art.rewards,
        temp_term=art.temp_term,
        energy_term=art.energy_term,
        setpoints=art.setpoints,
        meta=np.array(json.dumps(meta_dict)),
    )
    logger.info("Saved trajectory → %s", npz_path)

    fig = _make_analysis_figure(art, deadband_c=deadband_c)
    fig.savefig(png_path, dpi=110, bbox_inches="tight")
    plt.close(fig)
    logger.info("Saved figure → %s", png_path)

    # Per-zone + aggregate deviation stats (only for controlled zones, matching
    # the existing report schema).
    controlled_set = set(art.controlled_zones)
    per_zone: list[dict[str, Any]] = []
    controlled_cols: list[int] = []
    for j, zl in enumerate(art.zone_names):
        if zl in controlled_set or not art.controlled_zones:
            sp_col = art.setpoints[:, j]
            if np.isnan(sp_col).all():
                continue
            stats = _compute_deviation_stats(
                art.zone_temperatures[:, j],
                np.where(np.isnan(sp_col), art.zone_temperatures[:, j], sp_col),
                deadband_c,
            )
            per_zone.append({"zone": zl, **stats})
            controlled_cols.append(j)

    if controlled_cols:
        z_ctrl = art.zone_temperatures[:, controlled_cols]
        s_ctrl = art.setpoints[:, controlled_cols]
        mask = np.isnan(s_ctrl)
        s_ctrl = np.where(mask, z_ctrl, s_ctrl)
        aggregate = _compute_deviation_stats(z_ctrl.ravel(), s_ctrl.ravel(), deadband_c)
    else:
        aggregate = {}

    actuator_stats = {
        "n_actuators": int(art.actions.shape[1]) if art.actions.ndim == 2 else 0,
        "mean": (
            [float(x) for x in art.actions.mean(axis=0)] if art.actions.size else []
        ),
        "frac_at_min": (
            [
                float(x)
                for x in (art.actions <= art.actions.min(axis=0) + 1e-6).mean(axis=0)
            ]
            if art.actions.size
            else []
        ),
        "frac_at_max": (
            [
                float(x)
                for x in (art.actions >= art.actions.max(axis=0) - 1e-6).mean(axis=0)
            ]
            if art.actions.size
            else []
        ),
    }
    report: dict[str, Any] = {
        "building_type": art.building_type,
        "building_id": art.building_id,
        "climate_zone": art.climate_zone,
        "task": art.task,
        "episode_length": int(art.rewards.shape[0]),
        "temperatures": {"per_zone": per_zone, "aggregate": aggregate},
        "actions": actuator_stats,
        "reward": {
            "total": float(art.rewards.sum()),
            "mean_per_step": float(art.rewards.mean()),
            "temp_term_mean": float(art.temp_term.mean()),
            "energy_term_mean": float(art.energy_term.mean()),
        },
    }
    json_path.write_text(json.dumps(report, indent=2))
    logger.info("Saved report → %s", json_path)
    return report


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter
    )
    p.add_argument("--building-type", required=True)
    p.add_argument(
        "--building-id",
        default=None,
        help="Explicit building ID.  Omit together with --n-buildings to auto-select.",
    )
    p.add_argument("--climate-zone", type=int, required=True)
    p.add_argument(
        "--n-buildings",
        type=int,
        default=1,
        help="When --building-id is absent, analyze this many test-split buildings "
        "matching (--building-type, --climate-zone).",
    )
    p.add_argument("--task", default="task1")
    p.add_argument(
        "--run-period", default="full_year", choices=["full_year", "winter", "summer"]
    )
    p.add_argument(
        "--base-dir",
        type=Path,
        default=Path(os.environ.get("SCRATCH", "outputs")),
        help="Root directory; results go under <base_dir>/b2b/analysis/tuned_controllers/",
    )
    p.add_argument("--output-dir", required=False, default=None, type=Path)
    p.add_argument(
        "--deadband-c",
        type=float,
        default=1.0,
        help="Deadband half-width for satisfaction stats.",
    )
    return p.parse_args(argv)


def main(argv: list[str] | None = None) -> None:
    logging.basicConfig(
        level=logging.INFO, format="%(asctime)s %(name)s %(levelname)s: %(message)s"
    )
    args = parse_args(argv)

    output_dir = (
        args.output_dir
        if args.output_dir is not None
        else args.base_dir / "b2b" / "analysis" / "tuned_controllers"
    )

    if args.building_id is not None:
        building_ids = [args.building_id]
    else:
        building_ids = select_test_buildings(
            args.building_type, args.climate_zone, args.n_buildings
        )
        if not building_ids:
            logger.error(
                "No test buildings found for %s / cz=%d",
                args.building_type,
                args.climate_zone,
            )
            sys.exit(1)
        logger.info(
            "Auto-selected %d buildings for %s cz=%d: %s",
            len(building_ids),
            args.building_type,
            args.climate_zone,
            building_ids,
        )

    for bid in building_ids:
        try:
            art = rollout_and_analyze(
                args.building_type,
                bid,
                args.climate_zone,
                args.task,
                run_period=args.run_period,
            )
            save_artifacts(art, output_dir, deadband_c=args.deadband_c)
        except Exception:
            logger.exception(
                "Rollout failed: %s / %s cz=%d",
                args.building_type,
                bid,
                args.climate_zone,
            )


