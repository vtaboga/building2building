#!/usr/bin/env python3
"""Full-year rollout + analysis for a single building with tuned reactive controllers.

Produces three artifacts per building, all written under ``--output-dir``:

* ``<building_id>_cz<cz>.npz`` — per-step zone temperatures, setpoints, outdoor
  temperature, energy, actions, rewards, and reward decomposition
  (``temp_term``, ``energy_term``).  Schema matches the pre-existing
  ``analysis/officemedium_tuned/`` artifacts so downstream tooling keeps working.
* ``<building_id>_cz<cz>_analysis.png`` — 4-panel summary figure:
  zone temps vs per-zone setpoints, outdoor dry-bulb, HVAC energy,
  and reward decomposition over the year.
* ``<building_id>_cz<cz>.report.json`` — per-zone and aggregate deviation
  statistics; individual files are merged into a building-type-level
  ``report.json`` by ``baselines.summarize_tuned_analysis``.

Usage::

    python -m baselines.analyze_tuned_controller \\
        --building-type OfficeMedium --building-id OfficeMedium-4949 \\
        --climate-zone 1 --task task1 \\
        --output-dir analysis/officemedium_tuned_v2
"""

from __future__ import annotations

import argparse
import json
import logging
import os
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Literal

import matplotlib.pyplot as plt
import numpy as np
import yaml

import building2building as b2b
from baselines.controllers.air_loop import AirLoopConfig, AirLoopPolicy
from baselines.controllers.unitary_hvac import UnitaryHvacConfig, UnitaryHvacPolicy

logger = logging.getLogger("analyze_tuned_controller")

VAV_BUILDING_TYPES = {"OfficeMedium"}
TUNED_CONFIGS_DIR = Path(__file__).parent / "configs" / "tuned_controllers"


def select_test_buildings(
    building_type: str, climate_zone: int, n: int, *, split: str = "test"
) -> list[str]:
    """Pick the first ``n`` test-split buildings for ``(building_type, climate_zone)``.

    For building types without an ASHRAE climate-zone assignment (e.g.
    ``SingleFamilyHouse``), the CZ filter is skipped and the first ``n``
    IDs are returned as-is.  Ordering follows ``list_buildings`` /
    ``list_buildings_by_climate_zone`` (deterministic metadata order) to
    keep runs reproducible across SLURM attempts.
    """
    if building_type in b2b.TYPES_WITHOUT_CLIMATE_ZONE:
        return b2b.list_buildings(building_type, split=split)[:n]
    matching = b2b.list_buildings_by_climate_zone(
        building_type, climate_zone, split=split
    )
    if len(matching) < n:
        logger.warning(
            "Only %d test buildings match %s / cz=%d (wanted %d) — returning what we have",
            len(matching),
            building_type,
            climate_zone,
            n,
        )
    return matching[:n]


@dataclass
class RolloutArtifacts:
    """In-memory form of everything saved to disk per building."""

    zone_temperatures: np.ndarray  # (T, n_zones)
    zone_names: list[str]
    outdoor_temperature: np.ndarray  # (T,)
    energy_electricity: np.ndarray  # (T,)
    energy_gas: np.ndarray  # (T,)
    actions: np.ndarray  # (T-1, n_act)
    action_names: list[str]
    rewards: np.ndarray  # (T-1,)
    temp_term: np.ndarray  # (T-1,) MSE across controlled zones
    energy_term: np.ndarray  # (T-1,) electricity + gas
    setpoints: np.ndarray  # (T, n_zones) per-zone target
    controlled_zones: list[str]
    building_type: str
    building_id: str
    climate_zone: int
    task: str


# ---------------------------------------------------------------------------
# Config loading (handles SFH cz0 quirk that run_reactive_control skips)
# ---------------------------------------------------------------------------


def _load_tuned_config(
    bt: str, cz: int, *, task: str
) -> UnitaryHvacConfig | AirLoopConfig:
    """Load a tuned controller config from disk.

    Mirrors the naming convention used after the 2026-04-20 re-tune:
    ``{air_loop|unitary_hvac}_{building_type_lower}_cz{cz}.yaml``.
    The ``_task1`` segment from the raw tuner output has been stripped
    when the configs were copied into the repo.
    """
    is_vav = bt in VAV_BUILDING_TYPES
    prefix = "air_loop" if is_vav else "unitary_hvac"
    path = TUNED_CONFIGS_DIR / f"{prefix}_{bt.lower()}_cz{cz}.yaml"
    if not path.exists():
        logger.warning(
            "No tuned config at %s — falling back to default %s",
            path,
            "AirLoopConfig" if is_vav else "UnitaryHvacConfig",
        )
        return AirLoopConfig() if is_vav else UnitaryHvacConfig()

    # ``yaml.unsafe_load`` tolerates the ``!!python/tuple`` tags that the
    # tuner's ``yaml.dump`` emits for ``weekend_days`` inside
    # ``target_schedule`` (the tuner should really use ``safe_dump`` — tracked
    # separately).  Safe here because these files are generated by our own
    # tuning pipeline.
    raw = yaml.unsafe_load(path.read_text())
    raw.pop("type", None)
    raw.pop("target_schedule", None)
    # Coerce int-as-float scalars the same way run_reactive_control does.
    coerced = {
        k: (float(v) if isinstance(v, (int, float)) and not isinstance(v, bool) else v)
        for k, v in raw.items()
    }
    return AirLoopConfig(**coerced) if is_vav else UnitaryHvacConfig(**coerced)


def _select_policy(bt: str, cz: int, task: str, env: Any):
    cfg = _load_tuned_config(bt, cz, task=task)
    if bt in VAV_BUILDING_TYPES:
        policy = AirLoopPolicy(cfg)  # type: ignore[arg-type]
    else:
        policy = UnitaryHvacPolicy(cfg)  # type: ignore[arg-type]
    policy.bind_env(env)
    return policy


# ---------------------------------------------------------------------------
# Observation parsing
# ---------------------------------------------------------------------------


def _find_index(obs_names: list[str], needle: str) -> int | None:
    for i, n in enumerate(obs_names):
        if n.strip().lower() == needle.strip().lower():
            return i
    return None


def _zone_temp_indices(obs_names: list[str]) -> tuple[list[int], list[str]]:
    indices: list[int] = []
    labels: list[str] = []
    prefix = "zone air temperature"
    for i, n in enumerate(obs_names):
        s = n.strip()
        if s.lower().startswith(prefix):
            indices.append(i)
            labels.append(s[len("ZONE AIR TEMPERATURE") :].strip() or f"zone_{i}")
    return indices, labels


def _setpoint_indices(obs_names: list[str]) -> tuple[list[int], list[str]]:
    """Return indices and zone-name labels for per-zone target setpoints.

    The observation naming convention is ``target_temperature <zone>``.
    """
    indices: list[int] = []
    labels: list[str] = []
    prefix = "target_temperature"
    for i, n in enumerate(obs_names):
        s = n.strip()
        if s.lower().startswith(prefix):
            indices.append(i)
            labels.append(s[len("target_temperature") :].strip() or f"setpoint_{i}")
    return indices, labels


def _extract_energy_series(
    observations: np.ndarray, obs_names: list[str]
) -> tuple[np.ndarray, np.ndarray]:
    e_idx = _find_index(obs_names, "energy_electricity")
    g_idx = _find_index(obs_names, "energy_gas")
    T = observations.shape[0]
    elec = (
        observations[:, e_idx].astype(np.float32)
        if e_idx is not None
        else np.zeros(T, np.float32)
    )
    gas = (
        observations[:, g_idx].astype(np.float32)
        if g_idx is not None
        else np.zeros(T, np.float32)
    )
    return elec, gas


# ---------------------------------------------------------------------------
# Stats / reward decomposition
# ---------------------------------------------------------------------------


def _compute_deviation_stats(
    zone_temps: np.ndarray, setpoints: np.ndarray, deadband_c: float = 1.0
) -> dict[str, float]:
    """Zone-temperature deviation statistics over the episode.

    Positive deviation = zone is warmer than setpoint (overshoot → overcooling
    demand in summer, over-heating in winter).  ``frac_in_deadband`` uses a
    symmetric ``|T − setpoint| ≤ deadband_c`` window.
    """
    dev = zone_temps - setpoints
    abs_dev = np.abs(dev)
    return {
        "mean_dev_c": float(dev.mean()),
        "std_dev_c": float(dev.std()),
        "pct_1": float(np.percentile(dev, 1)),
        "pct_5": float(np.percentile(dev, 5)),
        "pct_25": float(np.percentile(dev, 25)),
        "pct_50": float(np.percentile(dev, 50)),
        "pct_75": float(np.percentile(dev, 75)),
        "pct_95": float(np.percentile(dev, 95)),
        "pct_99": float(np.percentile(dev, 99)),
        "frac_in_deadband": float((abs_dev <= deadband_c).mean()),
        "frac_above_deadband": float((dev > deadband_c).mean()),
        "frac_below_deadband": float((dev < -deadband_c).mean()),
        "max_over_c": float(dev.max()),
        "max_under_c": float((-dev).max()),
    }


def _decompose_reward(
    zone_temps_controlled: np.ndarray,  # (T, n_controlled)
    setpoints_controlled: np.ndarray,  # (T, n_controlled)
    energy_elec: np.ndarray,  # (T,)
    energy_gas: np.ndarray,  # (T,)
) -> tuple[np.ndarray, np.ndarray]:
    """Reconstruct reward decomposition used by :func:`base_reward_function`.

    The live reward is ``-(MSE(zone − target) + energy_weight · (elec + gas))``.
    We return the two additive terms separately so the plot can show which one
    drives the loss over the year.  The terms are evaluated at post-step obs
    (i.e. length T, where T = episode_length + 1).  Callers downstream slice
    them to ``T-1`` to line up with actions/rewards.
    """
    temp_term = ((zone_temps_controlled - setpoints_controlled) ** 2).mean(axis=1)
    energy_term = energy_elec + energy_gas
    return temp_term.astype(np.float32), energy_term.astype(np.float32)


